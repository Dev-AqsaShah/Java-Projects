

// Primitive data types wo basic data types hoti hain jo Java language mein pehle se built-in hoti hain.
//  Ye simple values store karti hain jaise numbers, characters, aur true/false. Ye object nahi hoti aur inki value directly memory mein store hoti hai.

// Simple English:
// Primitive data types are the basic built-in data types in Java. They store simple values like numbers, characters,
//  and true/false. They are not objects, and their values are stored directly in memory.


//  Java mein 8 primitive data types hotay hain. Ye wo basic types hain jo directly memory mein value store kartay hain (object nahi hotay). Ye fast aur lightweight hotay hain.

// byte – Chhoti numbers ke liye (−128 se 127 tak)

// byte age = 20;


// short – Thoda bada number (−32,768 se 32,767 tak)

// short year = 2025;


// int – Normal integers ke liye (−2 billion se +2 billion tak)

// int salary = 50000;


// long – Bohat bade numbers ke liye

// long population = 8000000000L;


// float – Decimal numbers ke liye (accuracy thodi kam hoti hai)

// float price = 19.99f;


// double – Decimal numbers ke liye (zyada accurate)

// double marks = 92.75;


// boolean – Sirf true ya false values ke liye

// boolean isJavaFun = true;


// char – Single character (letter ya symbol) store karne ke liye

// char grade = 'A';
