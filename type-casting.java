// Type Casting

// Roman Urdu:
// Type casting ka matlab hai aik data type ki value ko doosray data type mein convert karna. Java mein do tarah ki type casting hoti hai — Implicit (Automatic) aur Explicit (Manual).

// Simple English:
// Type casting means converting a value from one data type to another. In Java, there are two types of type casting — Implicit (Automatic) and Explicit (Manual).

// Types of Type Casting:

// Implicit (Widening) – Java automatically converts a smaller data type into a larger one.

// int num = 10;
// double newNum = num; // automatic conversion (int → double)


// Explicit (Narrowing) – You manually convert a larger data type into a smaller one.

// double value = 9.78;
// int newValue = (int) value; // manual conversion (double → int)